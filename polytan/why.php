            <h3>THE PERFECT SURFACE FOR ATHLETIC ACHIEVEMENT</h3>
            <p>Our mission at Polytan has been to provide the perfect surface for athletic achievements, and we have been doing so for almost 50 years. What in 1969 started as a pioneering task in the area of synthetic running tracks has to the present day remained a passion that constantly pushes us forward to new levels of performance. Polytan is now one of the world’s leading manufacturers of synthetic sports surfaces: from running tracks to all-weather and soft-impact surfaces, to highly developed synthetic turf systems, we offer the full product range from a single source including installation, maintenance and service – Made in Germany.
            </p>
