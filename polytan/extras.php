            <p>Synthetic turf areas from Polytan are significantly less sensitive and much easier to care for than natural grass pitches; they do not need to be rested for periods of time, and can be used all year round. However, snow, ice and ground frost always cause problems â€“ and this is where turf heating can help.</p>
            <p>Winter weather such as snow, ice or hard frost can restrict the play properties of synthetic turf systems, and therefore also increase the risk of players becoming injured. When installing synthetic turf systems in colder regions with frequent snowfall, we therefore recommend putting in place turf heating. The turf heating Polytan offers together with our partner AST Eis- u. Solartechnik GmbH is not only effective even in the event of snow and ice, but also particularly energy-efficient.</p>
            <p>In contrast with traditional systems, special heating mats are used; these are installed directly below the elastic layer, thereby enabling lower supply temperatures and significantly shorter reaction times. As a result, the heat loss in Polytan Turf heating systems is around 50% lower than with traditional pipe systems that are installed deeper.</p>

